		<h1> Considerações finais</h1>

    <br>
    <p>A ideia principal do projeto é proporcionar segurança ao residente, por meio de um software e automatizando a maquete. Criamos um sistema capaz de monitorar, janelas, portas, portões e lâmpadas.</p>
    <p>Baseamos o projeto na atual situação de segurança pública em nosso pais e com dados obtidos da SSP, pode se notar um aumento de roubos e furtos no Brasil. </p>
    <p>Para que o protótipo se torne automatizado utilizamos, Arduino, servos motores, sensores magnéticos, jumpers para conectar os equipamentos ao arduino, Leds, sensores de presença, sensor de chuva, buzzer, motor de passo, Protoboard e madeira MDF.</p>
    <p>Utilizando uma base de aproximadamente 1,34 metros e 69 centímetros, construímos a maquete, 11 Leds devidos em 6 cômodos e a garagem, automatizamos 3 portas, 1 janela e o portão da garagem. Utilizando o site como o painel de controle, será por ele o gerenciamento dos equipamentos.</p>
    <p>O site funciona para o usuário cadastrado fazer o login e ter acesso as suas informações e ter acesso ao painel de controle para gerenciar e monitorar sua residência á distancia ou nela mesmo. </p>
    <p>O Site também conta com um gráfico de consumo para o usuário ter uma base de quantos KWH ele utilizou no dia, semana, quinzena, mês ou no ano.</p>
    <p>Para que essa ideia tomasse forma, foram utilizadas algumas linguagens de programação como Json, PHP, Java Script e C++ e o MySql um sistema de gerenciamento de banco de dados.</p>
    <br>

    <h1> Projetos futuros</h1>
    <br>
    <p> Este protótipo buscou não só tentar entender algumas questões relativas ao tema de segurança pública, como ao mesmo tempo levantar questões adicionais como a automação residencial a partir de arduino. </p>
    <p>Acredita-se que os seguintes pontos são interessantes de serem explorados:</p>
    <p>- Ampliar o protótipo para uma residência real.</p>
    <p>- Notificar o usuário caso houver alguma atividade suspeita na residência.</p> 
    <p>- Desenvolvimento deum hardware de uso próprio da empresa para fins de uso da empresa substituindo a placa arduino, porem com o mesmo micro controlador ATmega328P.</p> 

